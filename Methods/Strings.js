// .charAt()
// Returns the character in the string at whatever index is passed into charAt

// .charCodeAt()
// Returns the integer of the UTF-16 code of the character at whatever index is passed into charCodeAt

// .concat()
// This method concatenates the string arguments to the calling string and returns a new string

// .endsWith()
// This method determines whether a string ends with the characters of a specified string, returning true or false

// .includes()
// This method performs a case-sensitive search to determine whether one string may be found within another string, returning true or false

// .indexOf()
// This method returns the index within the string of the first occurrence of the specified value. It returns -1 if not found