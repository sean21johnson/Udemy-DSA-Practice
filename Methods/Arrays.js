// .concat()
// A method used to merge two or more arrays. It does not change the existing arrays, but instead returns a new array

// .copyWithin()
// A method used to shallow copy a part of an array to another location in the same array and return it without modifying its length

// Array.entries()
// Returns a new Array Iterator object that contains the key/value pairs for each index in the array

// .every()
// A method used to test whether all elements in the array pass the test implemented by the provided function. It returns a boolean value

// .fill()
// Changes all element in an array to a static value, from a start index(default 0) to an end index (default array.length). It returns the modified array.

// .filter()
// Creates a new array with all elements that pass the test implemented by the provided function